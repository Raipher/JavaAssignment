    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
 String content = textArea1.getText();
        String id = jTextField4.getText().trim();

        if (content.isEmpty() || content.startsWith("Error")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please generate a valid report first.");
            return;
        }

        // We need to find the student object to get their email address
        Student targetStudent = null;
        for(Student s : db) {
            if(s.getId().equalsIgnoreCase(id)) {
                targetStudent = s;
                break;
            }
        }

        if(targetStudent != null) {
            StudentReportGenerator gen = new StudentReportGenerator();
            gen.sendReportEmail(targetStudent, content); // Call the generator logic
            
            javax.swing.JOptionPane.showMessageDialog(this, "Email sent to: " + targetStudent.getEmail());
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Could not find student email.");
        }
    
    }//GEN-LAST:event_jButton7ActionPerformed

