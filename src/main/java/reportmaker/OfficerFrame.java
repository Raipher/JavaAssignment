    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:                                         
        String id = jTextField4.getText().trim();
        String semStr = jTextField2.getText().trim();

        try {
            int sem = Integer.parseInt(semStr);
            Student s = null;
            // Find Student
            for (Student st : db) {
                if (st.getId().equalsIgnoreCase(id)) {
                    s = st;
                    break;
                }
            }

            if (s == null) {
                textArea1.setText("Student not found.");
                return;
            }

            // Build Report
            StringBuilder sb = new StringBuilder();
            sb.append("ACADEMIC REPORT\n=========================\n");
            sb.append("Name:    " + s.getName() + "\n");
            sb.append("ID:      " + s.getId() + "\n");
            sb.append("Program: " + s.getProgram() + "\n\n");
            sb.append(String.format("%-8s %-25s %-5s %-5s\n", "Code", "Title", "Grd", "Pnt"));
            sb.append("--------------------------------------------------\n");

            List<Course> list = (sem > 0) ? s.getCoursesBySemester(sem) : s.getCourses();
            for (Course c : list) {
                String title = c.getTitle();
                if(title.length() > 20) title = title.substring(0, 17) + "..";
                
                sb.append(String.format("%-8s %-25s %-5s %-5.2f\n", 
                    c.getCode(), title, c.getGrade(), c.getPoint()));
            }
            sb.append("--------------------------------------------------\n");
            sb.append(String.format("Cumulative GPA: %.2f", s.getCGPA()));

            textArea1.setText(sb.toString());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Semester must be a number.");
        }
    
    
    }//GEN-LAST:event_jButton2ActionPerformed

