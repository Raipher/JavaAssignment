    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:                                                                                                                                                     
        String key = jTextField3.getText().trim().toLowerCase();
        
        if(key.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a keyword.");
            return;
        }

        int count = 0;
        double totalCGPA = 0;
        Set<String> programs = new HashSet<>();
        List<String> allCourses = new ArrayList<>(); // To store course names

        for (Student s : db) {
            if (s.getProgram().toLowerCase().contains(key)) {
                count++;
                totalCGPA += s.getCGPA();
                programs.add(s.getProgram());
                
                for(Course c : s.getCourses()) {
                    if(!allCourses.contains(c.getTitle())){
                        allCourses.add(c.getTitle());
                    }
                }
            }
        }
        
        Collections.sort(allCourses);

        StringBuilder sb = new StringBuilder();
        sb.append("DEPARTMENT ANALYTICS\n");
        sb.append("--------------------\n");
        sb.append("Keyword: " + key + "\n\n");
        
        if (count > 0) {
            sb.append("Total Students: " + count + "\n");
            sb.append(String.format("Average CGPA:   %.2f\n\n", (totalCGPA/count)));
            
            sb.append("PROGRAMS:\n");
            for(String p : programs) sb.append("- " + p + "\n");
            sb.append("\n");
            
            sb.append("COURSES OFFERED:\n");
            for(String c : allCourses) {
                sb.append("- " + c).append("\n");
            }
            
        } else {
            sb.append("No students found.");
        }
        
        textArea2.setText(sb.toString());

        
    }//GEN-LAST:event_jButton4ActionPerformed

