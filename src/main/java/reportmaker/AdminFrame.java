    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            
            String sid = jTextField1.getText().trim(); 
            String cid = jTextField2.getText().trim(); 
            String grade = jTextField3.getText().trim().toUpperCase(); 
            String semStr = jTextField4.getText().trim(); 
            
            if (sid.isEmpty() || cid.isEmpty() || grade.isEmpty() || semStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields are required!");
                return;
            }

            int sem = Integer.parseInt(semStr); 

            
            Student s = null;
            for(Student st : db) if(st.getId().equalsIgnoreCase(sid)) s = st;

            
            CourseDefinition c = null;
            for(CourseDefinition cd : catalog) if(cd.getId().equalsIgnoreCase(cid)) c = cd;

            if (s != null && c != null) {
                // 5. Convert Grade to Points
                double p = 0.0;
                switch(grade) { 
                    case "A": p=4.0; break; 
                    case "B": p=3.0; break; 
                    case "C": p=2.0; break; 
                    case "D": p=1.0; break; 
                    case "F": p=0.0; break;
                    default: 
                        JOptionPane.showMessageDialog(this, "Invalid Grade (Use A, B, C, D, F)");
                        return;
                }

                
                s.addCourse(c.getId(), c.getName(), c.getCredits(), grade, p, sem, c.getInstructor());
                
                JOptionPane.showMessageDialog(this, "Grade Saved Successfully!");
                
                
                refreshBulkTable(); 
                refreshRiskTable();
                
                // 8. Clear Inputs
                jTextField1.setText(""); jTextField2.setText(""); jTextField3.setText(""); jTextField4.setText("");

            } else {
                JOptionPane.showMessageDialog(this, "Error: Student ID or Course ID not found.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Semester must be a number.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

