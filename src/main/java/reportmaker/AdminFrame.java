    private void initCustomTables() {
        // Init Bulk Table Model
        bulkModel = (DefaultTableModel) jTable2.getModel();
        refreshBulkTable();

        // Init Risk Table Model
        riskModel = (DefaultTableModel) jTable3.getModel();
        refreshRiskTable();
    }
    
   
    private void refreshBulkTable() {
        bulkModel.setRowCount(0); // Clear existing rows
        for (Student s : db) {
            bulkModel.addRow(new Object[]{
                s.getId(), 
                s.getName(), 
                s.getProgram(), 
                String.format("%.2f", s.getCGPA())
            });
        }
    }

   
    private void refreshRiskTable() {
        riskModel.setRowCount(0); // Clear existing rows
        boolean found = false;
        for (Student s : db) {
            // Check risk criteria: CGPA < 2.0 OR Failed > 3 courses
            if (s.getCGPA() < 2.0 || s.getFailedCourseCount() > 3) {
                String reason = (s.getCGPA() < 2.0) ? "Low CGPA" : "High Fails";
                if (s.getCGPA() < 2.0 && s.getFailedCourseCount() > 3) reason = "BOTH";
                
                riskModel.addRow(new Object[]{
                    s.getId(), 
                    s.getName(), 
                    String.format("%.2f", s.getCGPA()), 
                    s.getFailedCourseCount(),
                    reason // "Status" column in your GUI
                });
                found = true;
            }
        }
        if (!found) {
            riskModel.addRow(new Object[]{"-", "All Eligible", "-", "-", "Good"});
        }
    }
