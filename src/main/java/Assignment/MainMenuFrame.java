    private void customInit() {
        setLocationRelativeTo(null);
        setResizable(false);
        setTitle("Course Recovery System - Main Menu");
        
        lblWelcome.setText("Welcome, " + currentUser.getFullName() + "!");
        
        setupPermissions();
    }

     private void setupPermissions() {
        boolean canManageUsers = accessControl.hasPermission(currentUser, "MANAGE_USERS");
        
        btnCourseRecovery.setEnabled(true);
        btnEligibilityCheck.setEnabled(true);
        btnAcademicReport.setEnabled(true);
        
        btnUserManagement.setEnabled(canManageUsers);
        
        if (!canManageUsers) {
            btnUserManagement.setToolTipText("You do not have permission to access this feature");
        }
    }
