    private void btnUserManagementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserManagementActionPerformed
        if (!accessControl.hasPermission(currentUser, "MANAGE_USERS")) {
            JOptionPane.showMessageDialog(this,
                "You do not have permission to manage user accounts.",
                "Access Denied",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        UserManagementFrame umf = new UserManagementFrame(
            authentication, accessControl, userManager, currentUser, userManagementService
        );
        umf.setVisible(true);
    }//GEN-LAST:event_btnUserManagementActionPerformed

