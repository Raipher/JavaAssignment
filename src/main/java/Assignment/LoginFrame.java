    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());

        // Second click: If already logged in, enter main menu
        if (username.isEmpty() || password.isEmpty()) {
            showError("Please enter username and password.");
            return;
        }
        // First click: Perform login validation
        LoginService.LoginResult result = loginService.login(username, password);

        if (result.success) {
            showSuccess(result.message);
            
            MainMenuFrame mainMenu = new MainMenuFrame(
                authentication, 
                accessControl, 
                userManager, 
                result.user,
                userManagementService
            );
            mainMenu.setVisible(true);
            
            this.dispose();
            
        } else {
            showError(result.message);
            txtPassword.setText("");
            txtPassword.requestFocus();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnResetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPasswordActionPerformed
        String usernameOrEmail = txtUsername.getText().trim();

        if (usernameOrEmail.isEmpty()) {
            showError("Please enter your username or email first.");
            return;
        }

        LoginService.ResetRequestResult result = loginService.requestPasswordReset(usernameOrEmail);

        if (result.success) {
            showSuccess(result.message);
        } else {
            showError(result.message);
        }
    }//GEN-LAST:event_btnResetPasswordActionPerformed

    private void showError(String message) {
        lblMessage.setForeground(Color.RED);
        lblMessage.setText(message);
    }
    
    private void showSuccess(String message) {
        lblMessage.setForeground(new Color(0, 128, 0));
        lblMessage.setText(message);
    }
