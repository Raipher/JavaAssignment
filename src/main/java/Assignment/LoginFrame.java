    private void initServices() {
        userRepo = new UserFileRepository("users.txt");    // calls UserFileRepository constructor
    java.util.Map<String, User> users = userRepo.loadUsers();   // calls UserFileRepository.loadUsers()

    userManager = new UserManager(users, userRepo);   // calls UserManager constructor

    if (users.isEmpty()) {
        User admin = new User(   // calls User constructor
            0, 
            "admin",
            "Admin User",
            "admin123",
            "admin@example.com",
            ROLE_ADMIN
        );
        userManager.addUser(admin);   // calls UserManager.addUser()
        users = userManager.getAllUsers();   // calls UserManager.getAllUsers()
        }

        authentication = new Authentication(users);   // calls Authentication constructor

        accessControl = new AccessControl();    // calls AccessControl constructor
        setupRolePermissions();
        
        passwordResetService = new PasswordResetService();   // calls PasswordResetService constructor

        loginService = new LoginService(authentication, accessControl, userManager, passwordResetService, userRepo);
        
        userManagementService = new UserManagementService(userManager, accessControl, authentication);
    }
    
    private void setupRolePermissions() {
        accessControl.addPermissionToRole(ROLE_COURSE_ADMIN, "LOGIN");
        accessControl.addPermissionToRole(ROLE_COURSE_ADMIN, "MANAGE_USERS");
        
        accessControl.addPermissionToRole(ROLE_ACADEMIC_OFFICER, "LOGIN");
        accessControl.addPermissionToRole(ROLE_ACADEMIC_OFFICER, "MANAGE_USERS");
        
        accessControl.addPermissionToRole(ROLE_ADMIN, "LOGIN");
        accessControl.addPermissionToRole(ROLE_ADMIN, "MANAGE_USERS");
    }

    public boolean completePasswordReset(String token, String newPassword) {
        return loginService.completePasswordReset(token, newPassword);
    }
